                        <li class="news_story">Two years ago, Jeremy Lin coming to Houston made almost as big headlines as Dwight Howard's arrival.  Quietly, however, Lin has been designated to the bench and Patrick Beverley has become the starting guard in his place.  Many had hoped Lin would continue his superstar ways he had demonstrated in New York.  Of course, allowing Lin to come off the bench probably is a part of the strategy of the Rockets since many successful teams have a 6th man who could star on another team.  In fact, hard to believe as it is, just a few years ago James Harden (Houston Rocket's All-Star guard) played this role for the Oklahoma City Thunder.  It was also a given that Lin would have trouble succeeding with James Harden, since both players require orchestrating the game as part of their ability to succeed.  Thus a 6th-man role made sense for Lin, a humble player, and the scrappy, tough, defensive-minded Patrick Beverley became part of the starting lineup.</li>
