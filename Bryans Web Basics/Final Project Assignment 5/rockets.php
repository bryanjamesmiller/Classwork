                        <li class="news_story">Before Dwight Howard arrived in Houston, the Rockets already had an above-average center named Omer Asik.  Asik was a big part of the Rockets success in the past and felt upset that he was apparently going to be benched because of the superstar Howard coming to Houston.  In fact, Asik asked to be traded and Houston even tried; however, it was harder to trade Asik than many expected.  Ultimately, the Rockets took a page out of the Spurs' playbook who have started two centers ever since the days of Hakeem Olajuwon, who had to deal with David Robinson and Tim Duncan.  Ever since, the Spurs have almost always started two centers (though Tim Duncan has always played power forward).  In any case, putting both Dwight Howard and Omer Asik in the game at the same time (and even starting both players) seemed to help ease the situation over.  Although Asik did not always get the chance to start, Asik showed his high level of play when given the opportunity to get some minutes and probably remains a great option for many teams in need of an upgrade at center.</li>
